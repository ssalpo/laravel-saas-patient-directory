        function placeNavigationSection (el, distance) {
            const styles = {
                'transition': 'all 400ms ease-in-out'
            }

            el.css($(window).scrollTop() > distance + 80 ? {
                ...styles,
                'position': 'sticky',
                'top': $(window).width() > 1024 ? 63 : ($(window).width() <= 380 ? 0 : 78),
                'z-index': 100,
            } : {
